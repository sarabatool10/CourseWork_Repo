public void newfeed() throws FileNotFoundException, IOException{
    Queue ob=new Queue();
    ob.ENqueue(name.getText());
    ob.ENqueue(phone.getText());
    ob.ENqueue(msg.getText());
    File obj=new File("totalfeed.txt");
    Scanner scan=new Scanner(obj);
    String a=scan.nextLine();
    int check=Integer.valueOf(a)+1;
    scan.close();
    File feed=new File("feeback"+check+".txt");
    BufferedWriter bf1=new BufferedWriter(new FileWriter(obj));
    
    bf1.write(String.valueOf(check));
    bf1.close();
    BufferedWriter bf=new BufferedWriter(new FileWriter(feed));
    bf.write(ob.DEqueue().data+"!");
    bf.write(ob.DEqueue().data+"!");
    bf.write(ob.DEqueue().data);
    bf.close();
    JOptionPane.showMessageDialog(null, "Feedback added successfully");
    this.setVisible(false);
}
    
